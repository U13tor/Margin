// HostCore impl — bootstrap order per docs/05 §9.
//   Logger → Settings → EventBus → Tray → Keyring → CryptoPool →
//   PluginManager(per-plugin HostServices wrappers).
// Shutdown reverses this; Keyring::clearMemory zeroizes the process-static
// master cache after the crypto pool drops its in-memory copy.

